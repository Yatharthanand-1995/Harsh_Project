// Helper function to get available festival types
export function getAvailableFestivals(): FestivalType[] {
  const festivals = new Set<FestivalType>()
  PRODUCTS.forEach((p) => {
    if (p.festivalType) {
      festivals.add(p.festivalType)
    }
  })
  return Array.from(festivals).sort()
}

